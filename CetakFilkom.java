    private void jButton9ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton9ActionPerformed
        // TODO add your handling code here:
        String namaMenu = namaMenuTextField.getText();
        String tipeMenu = cetakButton.isSelected()? "CETAK" : "FOTOCOPY";
        int hargaSatuan = Integer.parseInt(hargaSatuanTextField.getText());
        
        listMenu.add(new Menu(tipeMenu, namaMenu, hargaSatuan));
        dtm.setRowCount(0);
        for (int i = 0; i < listMenu.size(); i++) {
            Object[] object = {listMenu.get(i).namaMenu, listMenu.get(i).tipeMenu, listMenu.get(i).hargaSatuan};
            dtm.addRow(object);
        }
        
        clearField();
    }//GEN-LAST:event_jButton9ActionPerformed

