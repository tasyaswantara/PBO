    private void jButton16ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton16ActionPerformed
        // TODO add your handling code here:
        int dialogButton = JOptionPane.YES_NO_OPTION;
        int dialogResult = JOptionPane.showConfirmDialog(this, "Delete this menu", "Delete", dialogButton);
        if(dialogResult == 0) {
            dtm.removeRow(row);
            listMenu.remove(row);
            dtm.setRowCount(0);
            
            for (int i = 0; i < listMenu.size(); i++) {
                Object[] object = {listMenu.get(i).namaMenu, listMenu.get(i).tipeMenu, listMenu.get(i).hargaSatuan};
                dtm.addRow(object);
            }
        
            clearField();
        } else {
            
        }
    }//GEN-LAST:event_jButton16ActionPerformed

    private void jTable3MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable3MouseClicked
        // TODO add your handling code here:
        row = jTable3.getSelectedRow();
        column = jTable3.getColumnCount();
        
        System.out.println(row + "," + column);
        namaMenuTextField.setText(dtm.getValueAt(row, 0).toString());
        hargaSatuanTextField.setText(dtm.getValueAt(row, 2).toString());
        String tipeMenu = dtm.getValueAt(row, 1).toString();
        
        for (Enumeration<AbstractButton> buttons = buttonGroup6.getElements(); buttons.hasMoreElements();) {
            AbstractButton button = buttons.nextElement();
            if(button.getText().equalsIgnoreCase(tipeMenu)) {
                button.setSelected(true);
            } 
        }
    }//GEN-LAST:event_jTable3MouseClicked

